window.addEventListener('scroll', awal);

function awal() {
    var reveals = document.querySelectorAll('#awal1');
    for (var i = 0; i < reveals.length; i++) {

        var windowheight = window.innerHeight;
        var revealtop = reveals[i].getBoundingClientRect().top;
        var revealpoint = 10;

        if (revealtop < windowheight - revealpoint) {
            reveals[i].classList.add('muncul');
        } else {
            reveals[i].classList.remove('muncul');
        }
    }
};
